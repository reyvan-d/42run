int	ft_atoi(const char *str)
{
	int		k;
	int		neg;
	int		num;

	k = 0;
	num = 0;
	neg = 1;
	while (str[k] == ' ' || str[k] == '\t' || str[k] == '\n' ||
		str[k] == '\r' || str[k] == '\v' || str[k] == '\f')
		k++;
	if (str[k] == '-' || str[k] == '+')
	{
		neg = (str[k] == '-') ? -1 : 1;
		k++;
	}
	while ('0' <= str[k] && str[k] <= '9')
	{
		num = (num * 10) + (str[k] - '0');
		k++;
	}
	return (num * neg);
}
