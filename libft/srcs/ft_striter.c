#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	k;

	k = 0;
	if (s == NULL || f == NULL)
		return ;
	while (s[k])
	{
		f((char *)&s[k]);
		k++;
	}
}
