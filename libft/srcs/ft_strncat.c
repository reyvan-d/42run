#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	k;
	size_t	l;

	k = -1;
	l = 0;
	while (s1[l])
		l++;
	while (s2[++k] != '\0' && k < n)
		s1[k + l] = s2[k];
	s1[k + l] = '\0';
	return (s1);
}
