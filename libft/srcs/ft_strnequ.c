#include <string.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	k;

	k = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[k] != '\0' && s2[k] != '\0' && k < n)
	{
		if (s1[k] != s2[k])
			return (0);
		k++;
	}
	if (k != n && (s1[k] != '\0' || s2[k] != '\0'))
		return (0);
	return (1);
}
